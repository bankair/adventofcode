def process(input)
  input.split(/\n/).map! do |line|
    _patterns, output = line.split(' | ').map(&:split)
    output.count { |digit| [2, 4, 3, 7].include?(digit.size) }
  end.sum
end

def test(input, expected)
  actual = process(input)
  if actual != expected
    puts "ERROR: #{actual.inspect} != #{expected}"
    puts "INPUT:\n#{input}"
    exit 1
  end
  puts "SUCCESS => #{actual.inspect}"
end

